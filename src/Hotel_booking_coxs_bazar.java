    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        costtotal.setText(null);
        if(hotel_name.getSelectedItem().equals("None")) JOptionPane.showMessageDialog(null, "Select a Hotel");
        else if(check_in.getSelectedItem().equals("None")) JOptionPane.showMessageDialog(null, "Select Check In Date");
        else if(check_out.getSelectedItem().equals("None")) JOptionPane.showMessageDialog(null, "Select Check Out Date");
        else if(room_type.getSelectedItem().equals("None")) JOptionPane.showMessageDialog(null, "Select Room Type");
        else if(room_no.getSelectedItem().equals("None")) JOptionPane.showMessageDialog(null, "Select Number of Rooms");
        else{
        int cost=0;
        int chk_in=0,chk_out=0;
            if(check_in.getSelectedItem().equals("13/11/19")) chk_in=1;
            else if(check_in.getSelectedItem().equals("14/11/19")) chk_in=2;
            else if(check_in.getSelectedItem().equals("15/11/19")) chk_in=3;
            if(check_out.getSelectedItem().equals("14/11/19")) chk_out=2;
            else if(check_out.getSelectedItem().equals("15/11/19")) chk_out=3;
            else if(check_out.getSelectedItem().equals("16/11/19")) chk_out=4;
            else if(check_out.getSelectedItem().equals("17/11/19")) chk_out=5;
            else if(check_out.getSelectedItem().equals("18/11/19")) chk_out=6;
            else if(check_out.getSelectedItem().equals("19/11/19")) chk_out=7;
            else if(check_out.getSelectedItem().equals("20/11/19")) chk_out=8;
        if(chk_in>=chk_out) JOptionPane.showMessageDialog(null, "Please Select Valid Check Out Date");
        else
        {
            if (hotel_name.getSelectedItem().equals("Hotel Sea Crown")) 
            {
                if(room_type.getSelectedItem().equals("Single")) cost=1000;
                else cost=2000;
            }
            else if (hotel_name.getSelectedItem().equals("Long Beach Hotel")) 
            {
                if(room_type.getSelectedItem().equals("Single")) cost=1500;
                else cost=3000;
            }
            else
            {
                if(room_type.getSelectedItem().equals("Single")) cost=800;
                else cost=1500;
            }
            String rm_no=room_no.getSelectedItem().toString();
            int r_no=Integer.parseInt(rm_no);
            cost = cost*r_no;
            cost = cost * (chk_out - chk_in);
            String COST=String.format("%d", cost);
            costtotal.setText(COST);
            
        }
        }
    }//GEN-LAST:event_jButton2ActionPerformed
