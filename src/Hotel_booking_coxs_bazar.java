    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(hotel_name.getSelectedItem().equals("None")) JOptionPane.showMessageDialog(null, "Select a Hotel");
        else if(check_in.getSelectedItem().equals("None")) JOptionPane.showMessageDialog(null, "Select Check In Date");
        else if(check_out.getSelectedItem().equals("None")) JOptionPane.showMessageDialog(null, "Select Check Out Date");
        else if(room_type.getSelectedItem().equals("None")) JOptionPane.showMessageDialog(null, "Select Room Type");
        else if(room_no.getSelectedItem().equals("None")) JOptionPane.showMessageDialog(null, "Select Number of Rooms");
        else
        {
            ArrayList<String> lines = new ArrayList<String>();
            String line;
            int chk_in=0,chk_out=0;
            if(check_in.getSelectedItem().equals("13/11/19")) chk_in=1;
            else if(check_in.getSelectedItem().equals("14/11/19")) chk_in=2;
            else if(check_in.getSelectedItem().equals("15/11/19")) chk_in=3;
            if(check_out.getSelectedItem().equals("14/11/19")) chk_out=2;
            else if(check_out.getSelectedItem().equals("15/11/19")) chk_out=3;
            else if(check_out.getSelectedItem().equals("16/11/19")) chk_out=4;
            else if(check_out.getSelectedItem().equals("17/11/19")) chk_out=5;
            else if(check_out.getSelectedItem().equals("18/11/19")) chk_out=6;
            else if(check_out.getSelectedItem().equals("19/11/19")) chk_out=7;
            else if(check_out.getSelectedItem().equals("20/11/19")) chk_out=8;
            if(chk_in>=chk_out) JOptionPane.showMessageDialog(null, "Please Select Valid Check Out Date");
            else
            {
                int nod=chk_out-chk_in;
                try {
                    File file;
                    if(hotel_name.getSelectedItem().equals("Hotel Sea Crown")) 
                        file= new File("C:\\Users\\PC\\Documents\\NetBeansProjects\\Travel_management_system\\db\\hotel sea crown.txt");
                    else if(hotel_name.getSelectedItem().equals("Long Beach Hotel")) 
                        file= new File("C:\\Users\\PC\\Documents\\NetBeansProjects\\Travel_management_system\\db\\long beach hotel.txt");
                    else
                        file= new File("C:\\Users\\PC\\Documents\\NetBeansProjects\\Travel_management_system\\db\\grace cox smart hotel.txt");
                    FileReader fr = new FileReader(file);
                    BufferedReader br = new BufferedReader(fr);
                    String date, rm_tp, rm_no, rm_no_temp = room_no.getSelectedItem().toString();
                    int rn1, rn2 = Integer.parseInt(rm_no_temp);
                    boolean flag = true;
                    while ((line = br.readLine()) != null) {
                        date = line;
                        rm_tp = br.readLine();
                        rm_no = br.readLine();
                        if (check_in.getSelectedItem().equals(date) && room_type.getSelectedItem().equals(rm_tp)) {
                            for (int i = 0; i < nod; ++i) {
                                if (i != 0) {
                                    date = br.readLine();
                                    rm_tp = br.readLine();
                                    rm_no = br.readLine();
                                }
                                rn1 = Integer.parseInt(rm_no);
                                if (rn2 > rn1) {
                                    flag = false;
                                    break;
                                } else {
                                    rn1 = rn1 - rn2;
                                    rm_no = String.format("%d", rn1);
                                    lines.add(date);
                                    lines.add(rm_tp);
                                    lines.add(rm_no);
                                }
                            }
                            if (!flag) {
                                JOptionPane.showMessageDialog(null, "Sorry!! Selected rooms are booked!!");
                                break;
                            } else {
                                JOptionPane.showMessageDialog(null, "Booking Confirmed!!");
                            }
                        } else {
                            lines.add(date);
                            lines.add(rm_tp);
                            lines.add(rm_no);
                        }
                    }
                    br.close();
                    fr.close();
                    if(flag)
                    {
                        FileWriter fw = new FileWriter(file, false);
                        PrintWriter pw = new PrintWriter(fw);
                        for (String str : lines) {
                            pw.println(str);
                        }
                        pw.close();
                        fw.close();
                    }
                    new Menu().setVisible(true);
                    this.dispose();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error");
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

